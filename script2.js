// Contact Form Handling
const contactForm = document.getElementById('contactForm');
const formSuccess = document.getElementById('formSuccess');
const submitBtn = document.getElementById('submitBtn');

// Form submission timestamp for anti-bot
let formLoadTime = Date.now();

if (contactForm) {
    contactForm.addEventListener('submit', function(e) {
        e.preventDefault();
        
        // Anti-bot: Check honeypot field
        const honeypot = document.getElementById('website');
        if (honeypot && honeypot.value !== '') {
            console.log('Bot detected');
            return false;
        }
        
        // Anti-bot: Check if form was submitted too quickly (less than 3 seconds)
        const submitTime = Date.now();
        if (submitTime - formLoadTime < 3000) {
            console.log('Form submitted too quickly');
            return false;
        }
        
        // Validate form
        if (!validateForm()) {
            return false;
        }
        
        // Show loading state
        submitBtn.disabled = true;
        submitBtn.classList.add('loading');
        
        // Collect form data
        const formData = new FormData();
        formData.append('nombre', document.getElementById('nombre').value.trim());
        formData.append('email', document.getElementById('email').value.trim());
        formData.append('telefono', document.getElementById('telefono').value.trim());
        formData.append('servicio', document.getElementById('servicio').value);
        formData.append('mensaje', document.getElementById('mensaje').value.trim());
        formData.append('whatsappConsent', document.getElementById('whatsappConsent').checked ? 'si' : 'no');
        formData.append('website', honeypot ? honeypot.value : '');
        
        // Send form via PHP
        fetch('send-email.php', {
            method: 'POST',
            body: formData
        })
        .then(response => response.json())
        .then(data => {
            if (data.success) {
                // Show success message
                contactForm.style.display = 'none';
                formSuccess.style.display = 'block';
                
                // Reset form after 5 seconds
                setTimeout(() => {
                    contactForm.reset();
                    contactForm.style.display = 'block';
                    formSuccess.style.display = 'none';
                    formLoadTime = Date.now();
                }, 5000);
            } else {
                alert(data.message || 'Error al enviar el mensaje. Por favor intenta de nuevo.');
            }
        })
        .catch(error => {
            console.error('Error:', error);
            alert('Error de conexión. Por favor intenta de nuevo.');
        })
        .finally(() => {
            // Reset button
            submitBtn.disabled = false;
            submitBtn.classList.remove('loading');
        });
    });
    
    // Real-time validation
    const inputs = contactForm.querySelectorAll('input, select, textarea');
    inputs.forEach(input => {
        input.addEventListener('blur', function() {
            validateField(this);
        });
        input.addEventListener('input', function() {
            if (this.parentElement.classList.contains('show-error')) {
                validateField(this);
            }
        });
    });
}

function validateForm() {
    let isValid = true;
    const nombre = document.getElementById('nombre');
    const email = document.getElementById('email');
    const servicio = document.getElementById('servicio');
    const mensaje = document.getElementById('mensaje');
    
    if (!validateField(nombre)) isValid = false;
    if (!validateField(email)) isValid = false;
    if (!validateField(servicio)) isValid = false;
    if (!validateField(mensaje)) isValid = false;
    
    return isValid;
}

function validateField(field) {
    const parent = field.parentElement;
    let isValid = true;
    
    // Remove previous error state
    parent.classList.remove('show-error');
    field.classList.remove('invalid');
    
    if (field.required && !field.value.trim()) {
        isValid = false;
    } else if (field.type === 'email' && field.value) {
        const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!emailRegex.test(field.value)) {
            isValid = false;
        }
    } else if (field.minLength && field.value.length < field.minLength) {
        isValid = false;
    }
    
    if (!isValid) {
        parent.classList.add('show-error');
        field.classList.add('invalid');
    }
    
    return isValid;
}
